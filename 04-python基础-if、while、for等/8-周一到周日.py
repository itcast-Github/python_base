#1. 获取用户输入的数字
num = input("请输入一个数字1周一 2周二......:")

#2. 根据用户的数据，显示相应的信息
if num=="1":
    print("周一")
elif num=="2":
    print("周二")
elif num=="3":
    print("周3")
elif num=="4":
    print("周4")
elif num=="5":
    print("周5")
elif num=="6":
    print("周6")
elif num=="7":
    print("周7")
else:
    print("输入有误")
